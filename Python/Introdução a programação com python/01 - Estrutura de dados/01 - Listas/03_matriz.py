matriz = [
    [1, "a", 2],
    ["b", 3, 4],
    [6, 5, "c"] # criando uma matriz (lista de listas)
]

print(matriz[0])  # [1, "a", 2] # acessando a primeira linha da matriz
print(matriz[0][0])  # 1 # acessando o primeiro elemento da primeira linha
print(matriz[0][-1])  # 2 # acessando o último elemento da primeira linha
print(matriz[-1][-1])  # "c" # acessando o último elemento da última linha
