#– Listas com strings, acessando letras



# Um fator interessante é que podemos acessar as strings dentro da lista, letra por letra, usando um segundo índice.
#  Vejamos as listagens 6.38 e 6.39.

s = ["maçã", "banana", "laranja", "uva"]
print(s[0][0])  # Imprime a primeira letra da primeira string
print(s[0][1])  # Imprime a segunda letra da primeira string
print(s[1][1])  # Imprime a segunda letra da segunda string
print(s[2][2])  # Imprime a terceira letra da terceira string


