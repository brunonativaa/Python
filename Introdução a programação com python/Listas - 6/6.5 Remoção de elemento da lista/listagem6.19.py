# Remoção de elementos da lista Como o tamanho da lista pode variar, permitindo a adição de novos elementos, 
# podemos também retirar alguns elementos da lista, ou mesmo todos eles.

lista = ['a', 'b', 'c', 'd', 'e']

del lista[1]  # Remove o elemento na posição 1 (segundo elemento)

print(lista)

del lista[0]

print(lista)