# Podemos também verificar se todos os caracteres de uma string são letras mai- úsculas ou minúsculas usando isupper e islower,

# Verificação de maiúsculas e minúsculas

s = "ABC"
p = "abc"
e = "aBc"

print(s.isupper())
print(s.islower())
print(p.isupper())
print(p.islower())
print(e.isupper())
print(e.islower())