# Para definir uma nova função, utilizaremos a instrução def.

# Definição de uma nova função

def soma(a,b): # uso da instruçao "def" seguida pelo nome da função(especificando dois parametros "a" e "b")
    print(a + b) # usando a função print para exibir (a + b)

soma(2,9)
soma(7,8) # os parametros recebera valores na ordem de definição
soma(10,15)