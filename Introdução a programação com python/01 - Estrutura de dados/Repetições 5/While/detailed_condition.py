# Executa uma ação enquanto uma codição for verdadeira
# Loop infinito -> Quando um código não tem fim

contador = 0

while contador < 100: # Enquanto o contador for menor que 100
    contador = contador + 1 # Atribuindo o valor do antingo contador com o novo valor" 
    print(contador) # A forma de atribuição é a mesma que contador += 1

print("Acabou...")