# Podemos utilizar a função range para gerar listas simples. 
# A função range não retorna uma lista propriamente dita, mas um gerador ou generator.
#  Por enquanto, basta entender como podemos usá-la. Imagine um programa simples que imprime de 0 a 9 na tela (Listagem 6.27).
for v in range(10):
    print(v)




